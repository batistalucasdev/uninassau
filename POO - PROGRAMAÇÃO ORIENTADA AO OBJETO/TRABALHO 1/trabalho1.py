# while loop
i = 0
while i < 5:
    print(f"While loop: {i}")
    i += 1

# do-while simulation
i = 5
while True:
    print(f"Simulação Do-While: {i}")
    i -= 1
    if i <= 0:
        break

# for loop
for j in range(5):
    print(f"For loop: {j}")

# if statement
x = 10
print("x = ", x)
if x > 5:
    print("x é maior que 5")
else:
    print("x é menor que 5")
