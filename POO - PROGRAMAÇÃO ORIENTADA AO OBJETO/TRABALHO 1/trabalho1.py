# while loop
i = 0
while i < 5:
    print(f"Laço While: {i}")
    i += 1

# simulação do-while
i = 5
while True:
    print(f"Simulação Do-While: {i}")
    i -= 1
    if i <= 0:
        break

# for loop
for j in range(5):
    print(f"Laço For: {j}")

# condicional if
x = 10
print("x = ", x)
if x > 5:
    print("x é maior que 5")
else:
    print("x é menor que 5")