let i = 0;

// while loop
while (i < 5) {
    console.log("While loop: " + i);
    i++;
}

// do-while loop
do {
    console.log("Do-While loop: " + i);
    i--;
} while (i > 0);

// for loop
for (let j = 0; j < 5; j++) {
    console.log("For loop: " + j);
}

// if statement
let x = 10;
if (x > 5) {
    console.log("x is greater than 5");
} else {
    console.log("x is not greater than 5");
}

// switch-case
let y = 2;
switch (y) {
    case 1:
        console.log("y is 1");
        break;
    case 2:
        console.log("y is 2");
        break;
    default:
        console.log("y is not 1 or 2");
}
